public class UnaryOperators {
    public static void main(String[] args) {
        int number = 10;
        System.out.println("Valoarea este: " + number + ".");

        number = -3;
        System.out.println("Valoarea este: " + number + ".");

        number = -number; // schimbarea semnului
        System.out.println("Valoarea este: " + number + ".");

        number++; //opertor de incrementare(creste numarul cu valoarea 1) reprezentat prin ++
        System.out.println("Valoarea este: " + number + ".");

        number--; //opertor de decrementare(descreste numarul cu valoarea 1) reprezentat prin --
        System.out.println("Valoarea este: " + number + ".");

        boolean notOperator = true;
        System.out.println(notOperator);



    }
}
