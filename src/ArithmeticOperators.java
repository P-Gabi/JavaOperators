public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println("Avem numerele 4 si 5.");
        // Suma
        int sum = 5 + 4;
        System.out.println("Suma este: " +sum);

        //Scadere
        int diff = 5 -4;
        System.out.println("Diferenta ete: " + diff);

        //Inmultirea
        int mult = 5 * 4;
        System.out.println("Produsul lor este: " + mult);

        // !!!!!!!!-------------------------------
        //Impartirea '/' sau Restul impartirii '%'
        // !!!!!!!!-------------------------------

        //Impartirea
        int div = 10/2;
        System.out.println("\nImpartirea lui 10 la 2 este: " + div);

        //Restul
        int mod= 10 % 2;
        System.out.println("Restul impartirii este lui 10 la 2 este: "+ mod);



    }
}
